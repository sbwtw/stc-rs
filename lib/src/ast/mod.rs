#[derive(Debug, Eq, PartialEq, Clone)]
pub enum TypeClass {
    /// 'BIT', one bit type
    Bit,
    /// 'BOOL', boolean type
    Bool,
    /// 'SINT', 8 bits signed
    SInt,
    /// 'BYTE', 8 bits unsigned
    Byte,
    /// 'INT', 16 bits signed
    Int,
    /// 'UINT', 16 bits unsigned
    UInt,
    /// 'DINT', 32 bits signed
    DInt,
    /// 'UDINT' 32 bits unsigned
    UDInt,
    /// 'LINT', 64 bits signed
    LInt,
    /// 'ULINT', 64 bits unsigned
    ULInt,
    /// 'REAL', 32 bits float
    Real,
    /// 'LREAL' 64 bits float
    LReal,
    /// 'STRING' string type
    String,
    /// UserType
    UserType(RefCell<UserType>),
    /// ArrayType
    Array(RefCell<ArrayType>),
}

impl Hash for TypeClass {
    fn hash<H: Hasher>(&self, state: &mut H) {
        let tag = match self {
            TypeClass::Bit => 1,
            TypeClass::Bool => 2,
            TypeClass::Byte => 3,
            TypeClass::UInt => 4,
            TypeClass::Int => 5,
            _ => unimplemented!("{:?}", self),
        };

        tag.hash(state);

        // TODO: incomplete implements
        match self {
            TypeClass::UserType(user_type) => {
                user_type.borrow().name().hash(state);
            }
            TypeClass::Array(array_type) => {
                let array_type = array_type.borrow();
                let base_type = array_type.base_type().borrow();
                base_type.type_class().hash(state);
            }
            _ => {}
        }
    }
}

impl Display for TypeClass {
    fn fmt(&self, f: &mut Formatter<'_>) -> fmt::Result {
        match self {
            TypeClass::Bit => write!(f, "BIT"),
            TypeClass::Bool => write!(f, "BOOL"),
            TypeClass::SInt => write!(f, "SINT"),
            TypeClass::Byte => write!(f, "BYTE"),
            TypeClass::Int => write!(f, "INT"),
            TypeClass::UInt => write!(f, "UINT"),
            TypeClass::DInt => write!(f, "DINT"),
            TypeClass::UDInt => write!(f, "UDINT"),
            TypeClass::LInt => write!(f, "LINT"),
            TypeClass::ULInt => write!(f, "ULINT",),
            TypeClass::Real => write!(f, "REAL"),
            TypeClass::LReal => write!(f, "LREAL"),
            TypeClass::String => write!(f, "STRING"),
            TypeClass::UserType(u) => write!(f, "{}", u.borrow()),
            TypeClass::Array(arr) => write!(f, "{}", arr.borrow()),
        }
    }
}
